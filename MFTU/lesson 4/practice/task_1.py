# Draw any static scene that contains at least 5 different objects
# consisting of five or more primitives

import graphics as gr

window = gr.GraphWin("my_home", 600, 600)

sky = gr.Rectangle(gr.Point(0, 0), gr.Point(600, 300))
sky.setFill('blue')
earth = gr.Rectangle(gr.Point(0, 300), gr.Point(600, 600))
earth.setFill('dark grey')
cloud1 = gr.Circle(gr.Point(50, 50), 15)
cloud1.setFill('white')
cloud2 = gr.Circle(gr.Point(65, 50), 15)
cloud2.setFill('white')
cloud3 = gr.Circle(gr.Point(80, 50), 15)
cloud3.setFill('white')
cloud4 = gr.Circle(gr.Point(55, 60), 15)
cloud4.setFill('white')
cloud5 = gr.Circle(gr.Point(70, 60), 15)
cloud5.setFill('white')
sun = gr.Circle(gr.Point(450, 80), 35)
sun.setFill('yellow')
house = gr.Rectangle(gr.Point(125, 225), gr.Point(275, 375))
house.setFill('grey')
house.setWidth(5)
roof = gr.Polygon(gr.Point(125, 225), gr.Point(200, 170), gr.Point(275, 225))
roof.setFill('brown')
roof.setWidth(5)
win1 = gr.Rectangle(gr.Point(165, 265), gr.Point(200, 300))
win1.setFill('yellow')
win1.setWidth(5)
win2 = gr.Rectangle(gr.Point(200, 265), gr.Point(235, 300))
win2.setFill('yellow')
win2.setWidth(5)
win3 = gr.Rectangle(gr.Point(165, 300), gr.Point(200, 335))
win3.setFill('yellow')
win3.setWidth(5)
win4 = gr.Rectangle(gr.Point(200, 300), gr.Point(235, 335))
win4.setFill('yellow')
win4.setWidth(5)
tree1 = gr.Polygon(gr.Point(400, 450), gr.Point(450, 400), gr.Point(500, 450))
tree1.setFill('green')
tree1.setWidth(3)
tree2 = gr.Polygon(gr.Point(400, 425), gr.Point(450, 375), gr.Point(500, 425))
tree2.setFill('green')
tree2.setWidth(3)
tree3 = gr.Polygon(gr.Point(400, 400), gr.Point(450, 350), gr.Point(500, 400))
tree3.setFill('green')
tree3.setWidth(3)
tree4 = gr.Rectangle(gr.Point(445, 450), gr.Point(455, 490))
tree4.setFill('brown')
tree4.setWidth(3)

sky.draw(window)
earth.draw(window)
cloud1.draw(window)
cloud2.draw(window)
cloud3.draw(window)
cloud4.draw(window)
cloud5.draw(window)
sun.draw(window)
house.draw(window)
roof.draw(window)
win1.draw(window)
win2.draw(window)
win3.draw(window)
win4.draw(window)
tree1.draw(window)
tree2.draw(window)
tree3.draw(window)
tree4.draw(window)

window.getMouse()
window.close()
