# Когда нам может помочь range

winners = ['Max', 'Leo', 'Kate']

# простой перебор
for winner in winners:
    print(winner)
# что делать если нам нужно вывести менсто победителя?
# использовать while
# или есть способ лучше?
for i in range(len(winners)):
    print(i)
    print(winners[i])
